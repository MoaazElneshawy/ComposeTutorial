data class InfoItem(
    val title: String,
    val moreData: String,
)

@Composable
fun InfoRowItem(item: InfoItem, index: Int) {
    var isExpanded by remember {
        mutableStateOf(false)
    }
    val extraPadding by animateDpAsState(
        if (isExpanded) 30.dp else 0.dp,
        animationSpec = spring(
            dampingRatio = Spring.DampingRatioMediumBouncy,
            stiffness = Spring.StiffnessLow
        )
    )
    Box(
        modifier = Modifier
            .fillMaxWidth()
            .heightIn(min = 70.dp, max = Int.MAX_VALUE.dp)
            .padding(10.dp)
            .clip(RoundedCornerShape(10.dp))
            .background(Color.Cyan)
    ) {

        Column(
            horizontalAlignment = Alignment.Start,
            modifier = Modifier
                .padding(bottom = extraPadding.coerceAtLeast(0.dp), start = 10.dp, end = 10.dp)
        ) {
            Row(
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(10.dp),
                horizontalArrangement = Arrangement.SpaceBetween,
                verticalAlignment = Alignment.Top
            ) {
                Text(
                    text = index.toString(),
                    color = Color.Black,
                    fontSize = 14.sp
                )
                IconButton(onClick = {
                    isExpanded = !isExpanded
                }, modifier = Modifier.size(24.dp)) {
                    Icon(
                        imageVector = if (isExpanded) Icons.Filled.KeyboardArrowUp else Icons.Filled.KeyboardArrowDown,
                        contentDescription = "ShowAndHide"
                    )
                }
            }
            Spacer(modifier = Modifier.height(5.dp))
            Text(
                text = item.title,
                color = Color.Black,
                fontSize = 17.sp,
                fontWeight = FontWeight.ExtraBold
            )
            Spacer(modifier = Modifier.height(10.dp))
            if (isExpanded)
                Text(
                    text = item.moreData,
                    color = Color.Black,
                    fontSize = 13.sp
                )
        }
    }
}

